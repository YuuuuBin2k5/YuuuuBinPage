// Helper function to get translation
export const t = (language, path) => {
  const keys = path.split(".");
  let value = translations[language];

  for (const key of keys) {
    value = value?.[key];
    if (value === undefined) {
      console.warn(`Translation not found for path: ${path}`);
      return path;
    }
  }

  return value;
};
